<?php
$str="+7-(911)444-57-55";

echo "Вы ввели номер телефона  ".$str."<br><br>";;

$regexp="/^(8|\+7)[-]?[(]?(\d{3})[)]?[-]?(\d{3})[-]?(\d{2})[-]?(\d{2})$/iu";
if (preg_match($regexp,$str)){
   $q=preg_replace($regexp,"8$2$3$4$5",$str);
   echo "Все ок, получили номер телефона --- ".$q;
} else {
    echo "Вы ввели неправильный номер телефона";
}